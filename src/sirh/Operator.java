    private void btnUpdateReserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateReserActionPerformed
        int viewRow = tblRes.getSelectedRow();
        if(viewRow>=0){
            lblIdResUpdate.setText(String.valueOf(tblRes.getValueAt(viewRow, 0)));
            lblIdGuestResUpdate.setText(String.valueOf(tblRes.getValueAt(viewRow, 1)));
            lblSpecialOrderResUpdate.setText((String)tblRes.getValueAt(viewRow, 2));
            lblCheckInResUpdate.setText((String)tblRes.getValueAt(viewRow, 3));
            lblCheckOutResUpdate.setText((String)tblRes.getValueAt(viewRow, 4));
            lblPriceResUpdate.setText("IDR. " + String.valueOf(tblRes.getValueAt(viewRow, 5)));
            lblDiskonResUpdate.setText((String.valueOf(tblRes.getValueAt(viewRow, 6)))+" %");
            lblTotPriceResUpdate.setText("IDR. " + String.valueOf(tblRes.getValueAt(viewRow, 7)));
//            cbStatusResUpdate.setSelectedIndex(0);
            if(tblRes.getValueAt(viewRow, 8).toString().equalsIgnoreCase("unconfirmed")){
                cbStatusResUpdate.setSelectedIndex(0);
            } else {
                cbStatusResUpdate.setSelectedIndex(1);
            }
            jPanelSlider1.nextPanel(8, panelReservationUpdate, jPanelSlider1.left);
        } else {            
            JOptionPane.showMessageDialog(this, "Please select a row you want to update.");
        }
    }//GEN-LAST:event_btnUpdateReserActionPerformed

