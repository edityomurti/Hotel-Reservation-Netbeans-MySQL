    private void btnUpdateRoomActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateRoomActionPerformed
        int viewRow = tblRoom.getSelectedRow();
        System.out.println(viewRow);
        if(viewRow>=0){            
            spIdKamarUpdate.setValue(tblRoom.getValueAt(viewRow, 0));
            if(tblRoom.getValueAt(viewRow, 1).toString().equalsIgnoreCase("deluxe")){
                cbTipeKamarUpdate.setSelectedIndex(1);
            } else {
                cbTipeKamarUpdate.setSelectedIndex(0);
            }            
            spNoKamarUpdate.setValue(tblRoom.getValueAt(viewRow, 2)); 
            if(tblRoom.getValueAt(viewRow, 3).toString().equalsIgnoreCase("available")){
                cbStatusKamarUpdate.setSelectedIndex(0);
            } else {
                cbStatusKamarUpdate.setSelectedIndex(1);
            }
            jPanelSlider1.nextPanel(8, panelRoomUpdate, jPanelSlider1.left);
        } else {
            JOptionPane.showMessageDialog(this, "Please select a row you want to update.");
        }
        
    }//GEN-LAST:event_btnUpdateRoomActionPerformed

